int main()
{
  ll n; cin >> n;
  ll m = n;
  ll sum = 0;
  for (ll i = 1; i * i <= n; ++i) {
    if(m % i == 0) {
      sum += i;
      if(i != m/i) {
	sum += m/i;
      }
    }
  }
  sum -= n;
  if(sum == n) cout << "Perfect" << "\n";
  else cout << (sum < n ? "Deficient" : "Abundant") << "\n";
  
  return 0;
}
